--[[**Vehicle Voucher System**

This script allows players to redeem **vehicle voucher items** to receive a vehicle directly in their garage. When a voucher is used, the player performs a short **2-second unpack animation**, after which the vehicle is automatically registered in the database and stored in the **Legion Square garage**.

**Features**
• Vehicle vouchers tied to specific spawn codes
• 2-second “unpack” progress interaction when redeeming
• Automatically generates a license plate and registers the vehicle
• Stores vehicles directly in the garage system
• Admin command to give players vehicle vouchers
• Fully compatible with **QBCore**, **ox_inventory**, and **jg-advancedgarages**


Example:
`/giveitem id sultanrs_item 1